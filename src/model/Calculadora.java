package model;

public class Calculadora {

    public double sumar(double num_a, double num_b){
        return num_a+num_b;
    }

    public double restar(double num_a, double num_b){
        return num_a-num_b;
    }

    public double multiplicar(double num_a, double num_b){
        return num_a*num_b;
    }

    public double dividir(double num_a, double num_b){
        return num_a/num_b;
    }

}
